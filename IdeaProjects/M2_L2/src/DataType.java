public class DataType {
    public static void main(String[] args) {
        int number = 1;
        float number_2 = 2.0f;
        double number_3 = 3.0d;
        byte number_4 = 0;
        char character = 'a';
        boolean bl = true;
        long number_5 = 1000000000L;
        String name = "ha noi";

        System.out.println(number + "\n"
                + number_2 + "\n"
                + number_3 + "\n"
                + number_4 + "\n"
                + number_5 + "\n"
                + character + "\n"
                + bl + "\n"
                + name + "\n"
        );
    }
}
